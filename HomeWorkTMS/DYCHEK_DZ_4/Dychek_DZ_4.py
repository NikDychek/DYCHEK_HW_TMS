from collections import Counter

sequence = "01234567890123456789" # sequence(последовательность) - контейнер, элементы которого представляют некую последоватательность
counts = Counter(sequence)

# Вывод словаря из 3х самых часто встречаемых чисел
top3 = dict(counts.most_common(3))

print(top3)

